Recipy.create(recipe_name: 'Shrimp, Leek, and Spinach Pasta', ingrediants: 'fusilli pasta,
unsalted butter, kosher salt, black pepper, medium shrimp, lemon zest, heavy cream, and baby
spinach', instructions: 'Cook the pasta; drain and return it to the pot. Heat the butter in a large
skillet over medium heat. Add the leeks, salt, and pepper and cook, stirring occasionally, until the
leeks have softened, 3 to 5 minutes. Add the shrimp and lemon zest and cook, tossing frequently,
until the shrimp is opaque throughout, 4 to 5 minutes more. Add the cream and salt to the pasta in
the pot and cook over medium heat, stirring, until slightly thickened, 1 to 2 minutes. Add the
shrimp mixture and the spinach and toss to combine.')
